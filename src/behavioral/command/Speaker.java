package behavioral.command;

public class Speaker {

    public void turnOn() {
        System.out.println("打開喇叭");
    }

    public void turnOff() {
        System.out.println("關喇叭");
    }

    public void louder() {
        System.out.println("大聲一點~~~");
    }

    public void lower() {
        System.out.println("小聲...");
    }

}
